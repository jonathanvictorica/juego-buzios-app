    private void txtCantidadavenderKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadavenderKeyReleased
        try {
            if (Integer.parseInt(this.txtCantidadavender.getText()) <= 0) {

                this.txtCantidadavender.setText("");
            } else {
                this.txtCantidadavender.setText("" + this.validarcantidadrecurso((String) this.cboRecursooferta.getSelectedItem(), Integer.parseInt(this.txtCantidadavender.getText())));
            }
        } catch (NumberFormatException e) {
            this.txtCantidadavender.setText("");
            this.repaint();
        }
    }//GEN-LAST:event_txtCantidadavenderKeyReleased

    private void cmdOfertarenmercadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdOfertarenmercadoActionPerformed
        if (!"".equals(this.txtCantidadavender.getText())) {
            try {
                VenderRecurso venderrecurso = new VenderRecurso(this.vciudad, new Recurso((String) this.cboRecursooferta.getSelectedItem()), new Recurso((String) this.cboRecursoacambio.getSelectedItem()), Integer.parseInt(this.txtCantidadavender.getText()), this.chkOro.isSelected());
                venderrecurso.CUVenderRecurso();
                ConstruirEdificio.actualizarinterfazgrafica(this.vciudad);


                this.hide();
            } catch (SQLException ex) {
                Logger.getLogger(VVenderOferta.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(panelImage1, "Debe ingresar la cantidad que desea vender");
        }
    }//GEN-LAST:event_cmdOfertarenmercadoActionPerformed

    private int validarcantidadrecurso(String recurso, Integer cantidad) {

        for (int i = 0; i < this.vciudad.getCiudad().getCiudadrecurso().getRecurso().size(); i++) {
            if (this.vciudad.getCiudad().getCiudadrecurso().getRecurso().get(i).getNombrerecurso().equals(recurso)) {
                if (this.vciudad.getCiudad().getCiudadrecurso().getCantidad().get(i) < cantidad) {
                    return this.vciudad.getCiudad().getCiudadrecurso().getCantidad().get(i);
                } else {
                    return cantidad;
                }
            }
        }
        return 0;
    }
