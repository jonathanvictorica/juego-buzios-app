    private void cmdCambiarNombreCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCambiarNombreCiudadActionPerformed
        if (!"".equals(this.txtCambiarNombreCiudad.getText())) {
            try {
                CambiarNombreCiudad cambiarnombre = new CambiarNombreCiudad(this.verintendencia.getVciudad().getCiudad(), this.txtCambiarNombreCiudad.getText());
                cambiarnombre.CUcambiarnombreciudad();
                this.verintendencia.vciudad.setTitle(this.verintendencia.ciudadjugador.getNombre() + " [" + this.verintendencia.ciudadjugador.getIsla().getCoordenadax() + ":" + this.verintendencia.ciudadjugador.getIsla().getCoordenaday() + "]");

                verintendencia.vciudad.repaint();
                this.txtCambiarNombreCiudad.setText("");
            } catch (SQLException ex) {

            }
        } else {
            JOptionPane.showMessageDialog(panelImage1, "Tienes que ingresar un nombre para cambiar el nombre a tu ciudad.");
        }
    }//GEN-LAST:event_cmdCambiarNombreCiudadActionPerformed

