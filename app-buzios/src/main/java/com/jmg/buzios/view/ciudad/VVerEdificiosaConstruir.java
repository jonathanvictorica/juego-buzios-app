    private void verdetalleedificio(String nombreedificio) {
        EdificioNivel edificioelegido = null;
        for (int i = 0; i < this.edificionivel.size(); i++) {
            if (this.edificionivel.get(i).getEdificio().getNombre().equals(nombreedificio)) {
                edificioelegido = this.edificionivel.get(i);
                VerDetalleEdificio verdetalleedificio = new VerDetalleEdificio(vciudad, lugarciudad, edificioelegido, this);
                verdetalleedificio.CUverdetalleedificio();
                return;
            }
        }

    }

